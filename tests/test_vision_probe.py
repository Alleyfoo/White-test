def test_probe_main_json_single_model_capable(monkeypatch, capsys) -> None:
    monkeypatch.setattr(vp, "probe_ollama_model", lambda *a, **k: _capable_report())
    rc = vp.main(["--model", "stub:model", "--json"])
    out = capsys.readouterr().out
    assert rc == 0
    data = json.loads(out)
    assert isinstance(data, list) and data[0]["verdict"] == vp.CAPABLE


def test_probe_main_no_capable_returns_one(monkeypatch, capsys) -> None:
    monkeypatch.setattr(
        vp, "probe_ollama_model", lambda *a, **k: _capable_report(vp.TEXT_ONLY)
    )
    rc = vp.main(["--model", "blind:model"])
    assert rc == 1


def test_probe_main_all_models_rc0_if_any_capable(monkeypatch) -> None:
    monkeypatch.setattr(
        vp,
        "probe_ollama_models",
        lambda *a, **k: [_capable_report(vp.CAPABLE), _capable_report(vp.TEXT_ONLY)],
    )
    rc = vp.main([])
    assert rc == 0


def test_probe_main_list_error_returns_one(monkeypatch, capsys) -> None:
    def _boom(*a, **k):
        raise LLMError("could not list Ollama models at 'x': boom")

    monkeypatch.setattr(vp, "probe_ollama_models", _boom)
    rc = vp.main([])
    assert rc == 1
    assert "error:" in capsys.readouterr().err


