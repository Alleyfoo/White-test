def _decode_png_rgb(data: bytes) -> tuple[int, int, tuple[int, int, int]]:
    """Minimal RGB-PNG decoder: return (width, height, first_pixel)."""
    assert data[:8] == b"\x89PNG\r\n\x1a\n", "bad PNG signature"
    offset = 8
    width = height = 0
    idat = b""
    while offset < len(data):
        (length,) = struct.unpack(">I", data[offset : offset + 4])
        tag = data[offset + 4 : offset + 8]
        chunk = data[offset + 8 : offset + 8 + length]
        if tag == b"IHDR":
            width, height, bitdepth, colortype = struct.unpack(">IIBB", chunk[:10])
            assert bitdepth == 8 and colortype == 2, "expected 8-bit truecolor RGB"
        elif tag == b"IDAT":
            idat += chunk
        offset += 12 + length
    raw = zlib.decompress(idat)
    first = raw[1:4]  # skip the filter byte of the first scanline
    return width, height, (first[0], first[1], first[2])


class _MapStub:
    """A client whose answer is keyed by probe image bytes.

    Matches the ``generate_text(prompt, image_b64) -> str`` seam. Missing images
    fall back to the blind answer, so an empty map == a model that cannot see.
    """

    def __init__(self, answers_by_image: dict[str, str], *, raise_exc: BaseException | None = None) -> None:
        self.answers = answers_by_image
        self.raise_exc = raise_exc
        self.calls: list[tuple[str, str]] = []

    def generate_text(self, prompt: str, image_b64: str) -> str:
        self.calls.append((prompt, image_b64))
        if self.raise_exc is not None:
            raise self.raise_exc
        return self.answers.get(image_b64, "I cannot see the image")


def _map(answers_by_name: dict[str, str]) -> dict[str, str]:
    """Translate {probe_name: answer} -> {image_b64: answer} via the spec set."""
    return {spec.image_b64: answers_by_name[spec.name] for spec in vp._SPECS}


def _capable_report(verdict: str = vp.CAPABLE) -> vp.ProbeReport:
    return vp.ProbeReport(provider="ollama", model="stub:model", verdict=verdict, probes=[])


