def test_probe_client_seeing_model_is_capable() -> None:
    stub = _MapStub(_map({
        "color_red": "red",
        "color_blue": "blue",
        "count_3": "3",
        "blank": "a uniform gray image",
    }))
    report = vp.probe_client(stub, model="seeing")
    assert report.verdict == vp.CAPABLE
    assert len(report.probes) == 4
    assert all(p.passed for p in report.probes)
    # the model's answers are carried as evidence
    by = {p.name: p.answer for p in report.probes}
    assert by["color_red"] == "red" and by["blank"] == "a uniform gray image"
    assert len(stub.calls) == 4  # every probe fired


def test_probe_client_blind_model_is_text_only() -> None:
    stub = _MapStub({})  # every probe -> blind fallback answer
    report = vp.probe_client(stub, model="blind")
    assert report.verdict == vp.TEXT_ONLY
    # real-content probes failed (no error), blank was honest
    by = {p.name: p for p in report.probes}
    assert by["color_red"].passed is False and by["color_red"].error is None
    assert by["blank"].passed is True


def test_probe_client_hallucinating_model() -> None:
    stub = _MapStub(_map({
        "color_red": "red",
        "color_blue": "blue",
        "count_3": "3",
        "blank": "a small white mushroom",
    }))
    report = vp.probe_client(stub, model="halluc")
    assert report.verdict == vp.HALLUCINATING
    by = {p.name: p for p in report.probes}
    assert by["color_red"].passed is True
    assert by["blank"].passed is False  # invented an object


def test_probe_client_inconsistent_model() -> None:
    stub = _MapStub(_map({
        "color_red": "red",
        "color_blue": "green",   # wrong color
        "count_3": "two",        # wrong count
        "blank": "a blank gray image",
    }))
    report = vp.probe_client(stub, model="mixed")
    assert report.verdict == vp.INCONSISTENT


def test_probe_client_unreachable_model_is_error() -> None:
    stub = _MapStub({}, raise_exc=LLMError("connection refused"))
    report = vp.probe_client(stub, model="down")
    assert report.verdict == vp.ERROR
    # all real-content probes errored, none passed, evidence recorded
    by = {p.name: p for p in report.probes}
    assert by["color_red"].error is not None
    assert by["color_red"].passed is False
    assert by["color_red"].answer == ""


