def test_color_match_accepts_synonyms() -> None:
    assert vp._color_match("The dominant color is crimson.", "red") is True
    assert vp._color_match("navy blue", "blue") is True
    assert vp._color_match("green", "red") is False
    assert vp._color_match("a green leaf", "blue") is False


def test_count_match_accepts_digit_or_word() -> None:
    assert vp._count_match("I see 3 regions.", 3) is True
    assert vp._count_match("there are three", 3) is True
    assert vp._count_match("two", 3) is False
    assert vp._count_match("I cannot tell.", 3) is False


def test_answer_invents_object_distinguishes_assertion_from_negation() -> None:
    assert vp._answer_invents_object("a small white mushroom") is True
    assert vp._answer_invents_object("I see no mushroom here.") is False
    assert vp._answer_invents_object("a uniform gray image, nothing visible") is False
    assert vp._answer_invents_object("there is no visible plant or animal") is False


