def test_solid_png_round_trip() -> None:
    w, h, px = _decode_png_rgb(vp._solid_png(64, 64, (220, 50, 50)))
    assert (w, h) == (64, 64)
    assert px == (220, 50, 50)


def test_count_png_dimensions_and_first_region() -> None:
    w, h, px = _decode_png_rgb(vp._count_png(3, 96, 32))
    assert (w, h) == (96, 32)
    assert px == (220, 50, 50)  # first region is red


